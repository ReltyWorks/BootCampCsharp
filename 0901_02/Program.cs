        public string ChangeDOT() {
            List<string> edges = new List<string>();

            //행렬의 절반 > 대각선 포함 위쪽 삼각형만 순회
            for (int i = 0; i < adj.GetLength(0); i++) {
                for (int j = i; j < adj.GetLength(1); j++) {
                    if (adj[i, j] == 1) {
                        edges.Add($"{i} -- {j};");
                    }
                }
            }
            return "graph G {\n" + string.Join("\n", edges) + "\n}";
        }
